class ReportData:
    """从 HTML 报告提取的结构化数据"""

    def __init__(self):
        self.project_name = ""
        self.generator = ""
        self.model_settings = {}      # modelling settings
        self.turbulence_settings = {}  # turbulence
        self.gravity_settings = {}     # gravity
        self.fluid = ""
        self.global_settings = {}      # pressure, ambient temp, etc.
        self.geometry = []             # list of dicts: {name, type, level, x, y, z, sx, sy, sz, collapsed, heat_spec, heat_applied}
        self.fixed_flows = []          # list of dicts
        self.overall_cutouts = []      # list of dicts
        self.temp_monitors = {}        # {name: final_temp}
        self.source_temps = {}         # {name: final_temp}
        self.convergence_iters = 0


def parse_html_report(html_path):
    """解析 FloTHERM HTML 报告，返回 ReportData"""
    if not Path(html_path).exists():
        return None, f"文件不存在: {html_path}"

    with open(html_path, 'r', encoding='utf-8', errors='ignore') as f:
        content = f.read()

    data = ReportData()

    # --- Generator ---
    m = re.search(r'Generated By\s*</b>([^<]+)', content)
    if m:
        data.generator = m.group(1).strip()

    # --- Project Name ---
    m = re.search(r'Project Name\s*</b>\s*([^<]+)', content)
    if m:
        data.project_name = m.group(1).strip()

    # --- Model Settings ---
    model_section = re.search(
        r'Modelling Settings</b>(.*?)</p>\s*<p><b>Turbulence',
        content, re.DOTALL
    )
    if model_section:
        lines = model_section.group(1)
        settings = re.findall(r'\t(\S[^<]+)', lines)
        data.model_settings = {
            'flow_type': _nth(settings, 0, ''),
            'dimension': _nth(settings, 1, ''),
            'radiation': _nth(settings, 2, ''),
            'state': _nth(settings, 3, ''),
            'solar': _nth(settings, 4, ''),
        }

    # --- Turbulence ---
    turb_section = re.search(
        r'Turbulence Settings</b>(.*?)</p>\s*<p><b>Gravity',
        content, re.DOTALL
    )
    if turb_section:
        lines = turb_section.group(1)
        settings = re.findall(r'\t(\S[^<]+)', lines)
        data.turbulence_settings = {
            'type': _nth(settings, 0, ''),
            'model': _nth(settings, 1, ''),
        }

    # --- Gravity ---
    grav_section = re.search(
        r'Gravity Settings</b>(.*?)</p>\s*<p><b>(?:Project Fluid|Global)',
        content, re.DOTALL
    )
    if grav_section:
        lines = grav_section.group(1)
        settings = re.findall(r'\t(\S[^<]+)', lines)
        data.gravity_settings = {
            'type': _nth(settings, 0, ''),
            'direction': _nth(settings, 1, ''),
        }

    # --- Fluid ---
    m = re.search(r'Project Fluid\s*</b>\s*(\S+)', content)
    if m:
        data.fluid = m.group(1).strip()

    # --- Global Settings ---
    gm = re.search(r'Datum Pressure:\s*([\d.eE+-]+)\s*Pa', content)
    if gm:
        data.global_settings['datum_pressure'] = float(gm.group(1))
    gm = re.search(r'External Radiant Temperature:\s*([\d.]+)\s*degC', content)
    if gm:
        data.global_settings['ext_radiant_temp'] = float(gm.group(1))
    gm = re.search(r'External Ambient Temperature:\s*([\d.]+)\s*degC', content)
    if gm:
        data.global_settings['ext_ambient_temp'] = float(gm.group(1))

    # --- Geometry Table ---
    geo_section = re.search(
        r'Geometry Model\s*</font>(.*?)</table>',
        content, re.DOTALL
    )
    if geo_section:
        rows = re.findall(r'<tr>\s*(.*?)\s*</tr>', geo_section.group(1), re.DOTALL)
        for row in rows:
            cells = re.findall(r'<td[^>]*>\s*(.*?)\s*</td>', row, re.DOTALL)
            if len(cells) >= 12:
                entry = {
                    'name': _clean(cells[0]),
                    'type': _clean(cells[1]),
                    'level': _clean(cells[2]),
                    'x': _num(cells[3]),
                    'y': _num(cells[4]),
                    'z': _num(cells[5]),
                    'sx': _num(cells[6]),
                    'sy': _num(cells[7]),
                    'sz': _num(cells[8]),
                    'collapsed': _clean(cells[9]),
                    'heat_spec': _num(cells[10]),
                    'heat_applied': _num(cells[11]),
                }
                data.geometry.append(entry)

    # --- Fixed Flows ---
    ff_section = re.search(
        r'Fixed Flows\s*</font>.*?<table[^>]*>(.*?)</table>',
        content, re.DOTALL
    )
    if ff_section:
        rows = re.findall(r'<tr>\s*(.*?)\s*</tr>', ff_section.group(1), re.DOTALL)
        for row in rows:
            cells = re.findall(r'<td[^>]*>\s*(.*?)\s*</td>', row, re.DOTALL)
            if len(cells) >= 7 and 'Fixed Flows' not in cells[0]:
                data.fixed_flows.append({
                    'name': _clean(cells[0]),
                    'volume_flow': _num(cells[1]),
                    'mass_flow': _num(cells[2]),
                    'heat_flow': _num(cells[3]),
                    'direction': _clean(cells[4]),
                    'mean_temp': _num(cells[5]),
                    'max_temp': _num(cells[6]),
                })

    # --- Overall/Cutouts ---
    oc_section = re.search(
        r'Overall/Cutouts\s*</font>.*?<table[^>]*>(.*?)</table>',
        content, re.DOTALL
    )
    if oc_section:
        rows = re.findall(r'<tr>\s*(.*?)\s*</tr>', oc_section.group(1), re.DOTALL)
        for row in rows:
            cells = re.findall(r'<td[^>]*>\s*(.*?)\s*</td>', row, re.DOTALL)
            if len(cells) >= 15 and 'Domain' in cells[0]:
                data.overall_cutouts.append({
                    'name': _clean(cells[0]),
                    'face': _clean(cells[1]),
                    'max_solid_temp': _num(cells[-2]),
                    'mean_solid_temp': _num(cells[-1]),
                })

    # --- Temperature Monitor Summary (最终温度) ---
    tm_sections = re.split(r'Temperature Monitor Summary', content, flags=re.IGNORECASE)
    for section in tm_sections[1:]:
        header_match = re.search(
            r'<td[^>]*>\s*([^<]+?)\s*\n\s*\(degC\)', section
        )
        if header_match:
            monitor_name = header_match.group(1).strip()
            data_match = re.search(
                r'<tr>\s*<td[^>]*>\s*(\d+)\s*</td>\s*<td[^>]*>\s*([\d.eE+-]+)\s*</td>',
                section
            )
            if data_match:
                data.temp_monitors[monitor_name] = float(data_match.group(2))

    # --- Source Temperature (per-source per-iteration, 取最终迭代) ---
    # 找所有 Source Temperature 表格
    source_blocks = re.findall(
        r'Iteration Number\s*</td>\s*<td[^>]*>\s*Source Temperature\s*\(degC\)\s*</td>(.*?)</table>',
        content, re.DOTALL
    )
    for block in source_blocks:
        iter_rows = re.findall(
            r'<td[^>]*>\s*(\d+)\s*</td>\s*<td[^>]*>\s*([\d.eE+-]+)\s*</td>',
            block
        )
        if iter_rows:
            # 第一行是最高迭代 = 最终结果
            final_iter = int(iter_rows[0][0])
            final_temp = float(iter_rows[0][1])
            data.source_temps[f'Source_{len(data.source_temps)}'] = {
                'temp': final_temp,
                'iter': final_iter,
            }

    # --- Convergence: 最大迭代数 ---
    all_iters = re.findall(r'Iteration Number.*?<td[^>]*>\s*(\d+)\s*</td>', content)
    if all_iters:
        data.convergence_iters = max(int(x) for x in all_iters)

    return data, "OK"


