def generate_diff_report(diff, label_a, label_b, output_path, tol):
    """生成 HTML 对比报告"""
    html = []
    html.append('<html><head><meta charset="utf-8">')
    html.append(f'<title>Model Comparison: {label_a} vs {label_b}</title>')
    html.append('<style>')
    html.append('body { font-family: "Microsoft YaHei", Arial, sans-serif; margin: 20px; font-size: 14px; }')
    html.append('table { border-collapse: collapse; width: 100%; margin: 10px 0; }')
    html.append('th, td { border: 1px solid #ddd; padding: 6px 10px; text-align: left; }')
    html.append('th { background-color: #2c3e50; color: white; }')
    html.append('tr:nth-child(even) { background-color: #f8f8f8; }')
    html.append('.pass { color: #27ae60; }')
    html.append('.fail { color: #e74c3c; font-weight: bold; }')
    html.append('.warn { color: #f39c12; }')
    html.append('.summary-box { background: #ecf0f1; padding: 15px; border-radius: 8px; margin: 15px 0; }')
    html.append('.section-title { background: #34495e; color: white; padding: 8px 12px; margin-top: 20px; }')
    html.append('.identical { background: #d5f5e3; padding: 15px; border-radius: 8px; margin: 15px 0; text-align: center; font-size: 18px; }')
    html.append('.different { background: #fadbd8; padding: 15px; border-radius: 8px; margin: 15px 0; text-align: center; font-size: 18px; }')
    html.append('</style></head><body>')

    html.append(f'<h1>模型对比验证报告</h1>')
    html.append(f'<p>生成时间: {datetime.now().strftime("%Y-%m-%d %H:%M:%S")}</p>')
    html.append(f'<p>对比: <b>{label_a}</b> vs <b>{label_b}</b> | 数值容差: {tol}</p>')

    # 总结
    all_match = diff.diff_count == 0
    if all_match:
        html.append('<div class="identical">✓ 两个模型完全一致 — 所有项目匹配</div>')
    else:
        html.append(f'<div class="different">✗ 发现 {diff.diff_count} 处差异</div>')

    html.append(f'<div class="summary-box">')
    html.append(f'匹配: <b class="pass">{diff.match_count}</b> | ')
    html.append(f'差异: <b class="fail">{diff.diff_count}</b> | ')
    html.append(f'总计: {diff.match_count + diff.diff_count}')
    html.append(f'</div>')

    # 各 section 详情
    for sec in diff.sections:
        # section header
        fail_count = sum(1 for i in sec['items'] if not i['match'])
        if fail_count > 0:
            html.append(f'<div class="section-title">{sec["section"]} — <span class="fail">{fail_count} 处差异</span></div>')
        else:
            html.append(f'<div class="section-title">{sec["section"]} — <span class="pass">全部匹配</span></div>')

        html.append('<table>')
        html.append('<tr><th>项目</th><th>A</th><th>B</th><th>差异</th><th>状态</th></tr>')
        for item in sec['items']:
            status = '<span class="pass">✓</span>' if item['match'] else '<span class="fail">✗</span>'
            diff_val = item['diff'] if item['diff'] else ''
            diff_class = 'fail' if not item['match'] else ''
            html.append(
                f'<tr>'
                f'<td>{item["key"]}</td>'
                f'<td>{item["val_a"]}</td>'
                f'<td>{item["val_b"]}</td>'
                f'<td class="{diff_class}">{diff_val}</td>'
                f'<td>{status}</td>'
                f'</tr>'
            )
        html.append('</table>')

    html.append('</body></html>')

    with open(output_path, 'w', encoding='utf-8') as f:
        f.write('\n'.join(html))

    return all_match


def print_console_summary(diff, label_a, label_b):
    """控制台输出摘要"""
    _safe_print(f"\n{'='*60}")
    _safe_print(f"对比结果: {label_a} vs {label_b}")
    _safe_print(f"{'='*60}")

    for sec in diff.sections:
        fail_count = sum(1 for i in sec['items'] if not i['match'])
        status = "[OK] 全部匹配" if fail_count == 0 else f"[!!] {fail_count} 处差异"
        _safe_print(f"\n[{sec['section']}] {status}")
        for item in sec['items']:
            if not item['match']:
                _safe_print(f"  [X] {item['key']}: A={item['val_a']}  B={item['val_b']}  diff={item['diff']}")

    _safe_print(f"\n{'='*60}")
    if diff.diff_count == 0:
        _safe_print("结论: 两个模型完全一致")
    else:
        _safe_print(f"结论: 发现 {diff.diff_count} 处差异，请查看详细报告")
    _safe_print(f"{'='*60}")


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main():
    parser = argparse.ArgumentParser(
        description='FloTHERM HTML Report 对比验证 — 求解两个模型并对比报告',
        formatter_class=argparse.RawDescriptionHelpFormatter,
        epilog="""
示例:
  python compare_html_reports.py model_A.pdml model_B.pdml
  python compare_html_reports.py report_A.html report_B.html
  python compare_html_reports.py A.pdml B.floxml --tolerance 0.1
  python compare_html_reports.py A.pack B.pack --flotherm "C:/path/to/flotherm.exe"
        """
    )
    parser.add_argument('file_a', help='模型文件 A 或 HTML 报告 A')
    parser.add_argument('file_b', help='模型文件 B 或 HTML 报告 B')
    parser.add_argument('--flotherm', default=DEFAULT_FLOTHERM,
                        help='flotherm.exe 路径 (默认自动检测)')
    parser.add_argument('--tolerance', type=float, default=0.01,
                        help='数值对比容差 (默认 0.01)')
    parser.add_argument('-o', '--output', default=None,
                        help='对比报告输出路径 (默认 compare_result.html)')
    parser.add_argument('--output-dir', default=None,
                        help='求解输出目录 (默认 ./compare_output)')
    args = parser.parse_args()

    file_a = Path(args.file_a)
    file_b = Path(args.file_b)
    tol = args.tolerance

    if not file_a.exists():
        print(f"[FAIL] File not found: {file_a}")
        return 1
    if not file_b.exists():
        print(f"[FAIL] File not found: {file_b}")
        return 1

    is_html_a = file_a.suffix.lower() in ('.html', '.htm')
    is_html_b = file_b.suffix.lower() in ('.html', '.htm')

    # 确定 FloTHERM 路径
    flotherm_exe = Path(args.flotherm)
    if not is_html_a or not is_html_b:
        if not flotherm_exe.exists():
            # 尝试自动检测
            candidates = [
                r"D:\Program Files\Siemens\SimcenterFlotherm\2504\WinXP\bin\flotherm.exe",
                r"C:\Program Files\Siemens\SimcenterFlotherm\2504\WinXP\bin\flotherm.exe",
            ]
            for c in candidates:
                if Path(c).exists():
                    flotherm_exe = Path(c)
                    break
            else:
                print(f"[FAIL] flotherm.exe not found: {flotherm_exe}")
                print("  Use --flotherm to specify the path")
                return 1

    output_dir = Path(args.output_dir) if args.output_dir else Path('./compare_output')

    # --- 求解或直接使用 HTML ---
    if is_html_a:
        html_a = file_a
        label_a = file_a.stem
        _safe_print(f"A: Using existing report {file_a.name}")
    else:
        _safe_print(f"\n--- Solving Model A: {file_a.name} ---")
        html_a, ok_a, _ = solve_model(flotherm_exe, file_a, output_dir / 'a', 'model_a')
        label_a = file_a.stem
        if not ok_a and not html_a.exists():
            print(f"[FAIL] Model A solve failed, no report generated")
            return 1

    if is_html_b:
        html_b = file_b
        label_b = file_b.stem
        _safe_print(f"B: Using existing report {file_b.name}")
    else:
        _safe_print(f"\n--- Solving Model B: {file_b.name} ---")
        html_b, ok_b, _ = solve_model(flotherm_exe, file_b, output_dir / 'b', 'model_b')
        label_b = file_b.stem
        if not ok_b and not html_b.exists():
            print(f"[FAIL] Model B solve failed, no report generated")
            return 1

    # --- 解析 ---
    _safe_print(f"\n--- Parsing HTML Reports ---")
    data_a, status_a = parse_html_report(html_a)
    data_b, status_b = parse_html_report(html_b)

    if data_a is None:
        print(f"[FAIL] Parse error A: {status_a}")
        return 1
    if data_b is None:
        print(f"[FAIL] Parse error B: {status_b}")
        return 1

    _safe_print(f"A: {status_a} | Project={data_a.project_name} | Geometry={len(data_a.geometry)} | Monitors={len(data_a.temp_monitors)}")
    _safe_print(f"B: {status_b} | Project={data_b.project_name} | Geometry={len(data_b.geometry)} | Monitors={len(data_b.temp_monitors)}")

    # --- Compare ---
    _safe_print(f"\n--- Comparing (tolerance={tol}) ---")
    diff = compare_data(data_a, data_b, tol)

    # --- Output ---
    print_console_summary(diff, label_a, label_b)

    output_path = Path(args.output) if args.output else Path('compare_result.html')
    all_match = generate_diff_report(diff, label_a, label_b, output_path, tol)
    _safe_print(f"\nReport saved: {output_path}")

    return 0 if all_match else 1


if __name__ == '__main__':
    sys.exit(main())
