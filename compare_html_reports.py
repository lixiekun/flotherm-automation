class DiffResult:
    def __init__(self):
        self.sections = []   # list of {section, items: [{key, val_a, val_b, match, diff}]}
        self.match_count = 0
        self.diff_count = 0
        self.missing_count = 0


def compare_data(data_a, data_b, tol=0.01):
    """对比两份 ReportData，返回 DiffResult"""
    diff = DiffResult()

    # 1. Model Settings
    section = {'section': '模型设置', 'items': []}
    for key in ['flow_type', 'dimension', 'radiation', 'state', 'solar']:
        va = data_a.model_settings.get(key, '')
        vb = data_b.model_settings.get(key, '')
        match = va == vb
        section['items'].append({
            'key': key, 'val_a': va, 'val_b': vb,
            'match': match, 'diff': '' if match else '不一致'
        })
    diff.sections.append(section)

    # 2. Turbulence & Gravity
    section = {'section': '湍流/重力', 'items': []}
    for key in ['type', 'model']:
        va = data_a.turbulence_settings.get(key, '')
        vb = data_b.turbulence_settings.get(key, '')
        match = va == vb
        section['items'].append({
            'key': f'turbulence_{key}', 'val_a': va, 'val_b': vb,
            'match': match, 'diff': '' if match else '不一致'
        })
    for key in ['type', 'direction']:
        va = data_a.gravity_settings.get(key, '')
        vb = data_b.gravity_settings.get(key, '')
        match = va == vb
        section['items'].append({
            'key': f'gravity_{key}', 'val_a': va, 'val_b': vb,
            'match': match, 'diff': '' if match else '不一致'
        })
    diff.sections.append(section)

    # 3. Fluid & Global Settings
    section = {'section': '流体/全局设置', 'items': []}
    match = data_a.fluid == data_b.fluid
    section['items'].append({
        'key': 'fluid', 'val_a': data_a.fluid, 'val_b': data_b.fluid,
        'match': match, 'diff': '' if match else '不一致'
    })
    for key in ['datum_pressure', 'ext_radiant_temp', 'ext_ambient_temp']:
        va = data_a.global_settings.get(key)
        vb = data_b.global_settings.get(key)
        match = _num_eq(va, vb, tol)
        d = '' if match else _num_diff(va, vb)
        section['items'].append({
            'key': key, 'val_a': _fmt(va), 'val_b': _fmt(vb),
            'match': match, 'diff': d
        })
    diff.sections.append(section)

    # 4. Geometry
    section = {'section': '几何结构', 'items': []}
    geo_a = {g['name']: g for g in data_a.geometry}
    geo_b = {g['name']: g for g in data_b.geometry}
    all_names = sorted(set(list(geo_a.keys()) + list(geo_b.keys())))
    for name in all_names:
        ga = geo_a.get(name)
        gb = geo_b.get(name)
        if ga and gb:
            for dim in ['x', 'y', 'z', 'sx', 'sy', 'sz', 'heat_spec', 'heat_applied']:
                va = ga.get(dim)
                vb = gb.get(dim)
                match = _num_eq(va, vb, tol)
                d = '' if match else _num_diff(va, vb)
                section['items'].append({
                    'key': f'{name}.{dim}', 'val_a': _fmt(va), 'val_b': _fmt(vb),
                    'match': match, 'diff': d
                })
        elif ga:
            section['items'].append({
                'key': name, 'val_a': '存在', 'val_b': '缺失',
                'match': False, 'diff': 'B 中缺失'
            })
        else:
            section['items'].append({
                'key': name, 'val_a': '缺失', 'val_b': '存在',
                'match': False, 'diff': 'A 中缺失'
            })
    diff.sections.append(section)

    # 5. Fixed Flows
    section = {'section': 'Fixed Flows', 'items': []}
    for i in range(max(len(data_a.fixed_flows), len(data_b.fixed_flows))):
        fa = _nth(data_a.fixed_flows, i)
        fb = _nth(data_b.fixed_flows, i)
        if fa and fb:
            for key in ['volume_flow', 'mass_flow', 'heat_flow', 'mean_temp', 'max_temp']:
                va = fa.get(key)
                vb = fb.get(key)
                match = _num_eq(va, vb, tol)
                d = '' if match else _num_diff(va, vb)
                section['items'].append({
                    'key': f'FixedFlow[{i}].{key}', 'val_a': _fmt(va), 'val_b': _fmt(vb),
                    'match': match, 'diff': d
                })
    diff.sections.append(section)

    # 6. Overall/Cutouts
    section = {'section': 'Overall/Cutouts', 'items': []}
    for i in range(max(len(data_a.overall_cutouts), len(data_b.overall_cutouts))):
        oa = _nth(data_a.overall_cutouts, i)
        ob = _nth(data_b.overall_cutouts, i)
        if oa and ob:
            for key in ['max_solid_temp', 'mean_solid_temp']:
                va = oa.get(key)
                vb = ob.get(key)
                match = _num_eq(va, vb, tol)
                d = '' if match else _num_diff(va, vb)
                label = f"{oa['name']}.{oa['face']}.{key}"
                section['items'].append({
                    'key': label, 'val_a': _fmt(va), 'val_b': _fmt(vb),
                    'match': match, 'diff': d
                })
    diff.sections.append(section)

    # 7. Temperature Monitors
    section = {'section': 'Temperature Monitor', 'items': []}
    all_monitors = sorted(set(
        list(data_a.temp_monitors.keys()) + list(data_b.temp_monitors.keys())
    ))
    for name in all_monitors:
        va = data_a.temp_monitors.get(name)
        vb = data_b.temp_monitors.get(name)
        match = _num_eq(va, vb, tol)
        d = '' if match else _num_diff(va, vb)
        section['items'].append({
            'key': name, 'val_a': _fmt(va), 'val_b': _fmt(vb),
            'match': match, 'diff': d
        })
    diff.sections.append(section)

    # 8. Source Temperatures
    section = {'section': 'Source Temperature', 'items': []}
    all_sources = sorted(set(
        list(data_a.source_temps.keys()) + list(data_b.source_temps.keys())
    ))
    for name in all_sources:
        sa = data_a.source_temps.get(name)
        sb = data_b.source_temps.get(name)
        if sa and sb:
            va = sa.get('temp')
            vb = sb.get('temp')
            match = _num_eq(va, vb, tol)
            d = '' if match else _num_diff(va, vb)
            section['items'].append({
                'key': name, 'val_a': _fmt(va), 'val_b': _fmt(vb),
                'match': match, 'diff': d
            })
    diff.sections.append(section)

    # 统计
    for sec in diff.sections:
        for item in sec['items']:
            if item['match']:
                diff.match_count += 1
            else:
                diff.diff_count += 1

    return diff


def _num_eq(a, b, tol=0.01):
    """比较两个数值是否在容差内相等"""
    if a is None and b is None:
        return True
    if a is None or b is None:
        return False
    if isinstance(a, str) or isinstance(b, str):
        return str(a) == str(b)
    return abs(float(a) - float(b)) <= tol


def _num_diff(a, b):
    if a is None or b is None:
        return 'N/A'
    try:
        return f'{abs(float(a) - float(b)):.6g}'
    except (ValueError, TypeError):
        return 'N/A'


