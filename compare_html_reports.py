def _safe_print(text):
    """Safe print for Windows GBK console"""
    try:
        print(text)
    except UnicodeEncodeError:
        print(text.encode('gbk', errors='replace').decode('gbk'))

def _nth(lst, idx, default=''):
    return lst[idx] if idx < len(lst) else default


def _clean(s):
    """清理 HTML cell 内容"""
    return s.replace('&nbsp;', '').replace('\n', '').strip()


def _num(s):
    """尝试转数字，失败返回 None"""
    s = _clean(s)
    if not s:
        return None
    try:
        return float(s)
    except ValueError:
        return None


def _fmt(v):
    if v is None:
        return '-'
    if isinstance(v, float):
        return f'{v:.6g}'
    return str(v)


