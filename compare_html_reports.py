def solve_model(flotherm_exe, input_path, output_dir, label):
    """求解模型，返回 (html_path, success, elapsed)"""
    input_path = Path(input_path)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    output_pack = output_dir / f"{label}.pack"
    output_html = output_dir / f"{label}.html"

    cmd = [
        str(flotherm_exe),
        "-b",
        str(input_path),
        "-z", str(output_pack),
        "-r", str(output_html),
    ]

    _safe_print(f"  Solving: {input_path.name} -> {output_html.name}")
    start = time.time()
    try:
        result = subprocess.run(cmd, capture_output=True, text=True, timeout=600)
        elapsed = time.time() - start
        if result.returncode == 0 and (output_pack.exists() or output_html.exists()):
            _safe_print(f"  [OK] Solved ({elapsed:.0f}s)")
            return output_html, True, elapsed
        else:
            _safe_print(f"  [FAIL] rc={result.returncode}")
            if result.stderr:
                _safe_print(f"    {result.stderr[:300]}")
            return output_html, False, elapsed
    except subprocess.TimeoutExpired:
        elapsed = time.time() - start
        _safe_print(f"  [FAIL] Timeout ({elapsed:.0f}s)")
        return output_html, False, elapsed


